a, b = 12, 5

print(a + b)
print(a - b)
print(a * b)
print(a / b) # impartire (va da float): 2.4
print(a // b) # impartire fara rest (va da int): 2
print(a % b) # restul imparitii (modulo): 2
print(a ** b) # ridicare la putere

