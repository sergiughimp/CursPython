# Tipuri de date Primitive (integer, double/float, boolean, char)

# String = sir de caractere delimitate de ' '
marca = 'Dacia Logan'
print(marca)
# afisare dimensiunea unui string
print(len(marca))
# scriem cu majuscule
print(marca.upper())
# scriem cu minuscule
print(marca.lower())

# interger = numar intreg (int)
an_fabricatie = 1995
print(an_fabricatie)

# double / float = numar zecimal
pret = 26.300
print(f'{pret}')

# boolean = true sau false
inmatriculata = True
inmatriculata = False
print(f'{inmatriculata}')

# char = un singur caracter
nota = 'A'
print(f'{nota}')

# concatenare string & integer
model = 1310
print(marca + 'model')

# concatenare string & string
model = '1311'
print(marca + model)